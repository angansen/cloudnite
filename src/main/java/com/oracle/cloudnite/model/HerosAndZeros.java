package com.oracle.cloudnite.model;

public class HerosAndZeros {
	String svp;
	String reps;
	String icon;
	int numberOfDeals;
	public String getSvp() {
		return svp;
	}
	public void setSvp(String svp) {
		this.svp = svp;
	}
	public String getReps() {
		return reps;
	}
	public void setReps(String reps) {
		this.reps = reps;
	}
	public int getNumberOfDeals() {
		return numberOfDeals;
	}
	public void setNumberOfDeals(int numberOfDeals) {
		this.numberOfDeals = numberOfDeals;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}


}
